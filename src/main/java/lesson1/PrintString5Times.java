package lesson1;

/*
1. Write a program which prints to the console some string 5 times.

"1) New string variable should be created.
2) Each string should be printed from a new line."

Using of loops is not required. You can you just print method 5 times.
 */
public class PrintString5Times {
    public static void main(String[] args) {
        String str = "Hello";
        System.out.println("str");
        System.out.println("str");
        System.out.println("str");
        System.out.println("str");
        System.out.println("str");
    }
}
